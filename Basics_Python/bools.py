# operators
print(8 > 10) # false
print(8 >= 10) # false
print(8 == 10) # false
print(8 <= 10) # true
print(8 < 10) # true

#if statement
num1 = 10
num2 = 5
if num1 > num2:
    print(num1, "is greater than ", num2)
else:
    print(num2, "is greater than ", num1)
# output 10 is greater than 5

#evaluate values and variables
print(bool(15 > 103)) # false

# almost all values are true